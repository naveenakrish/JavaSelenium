package Assignment1;

public class Swap {
    public static void main(String[] args) {
        int a=3;
        int b=5;
        int temp;
        System.out.println("before swap");
        System.out.println(a);
        System.out.println(b);
        System.out.println("--------------------------------------------------");
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swap");
        System.out.println(a );
        System.out.println(b);



    }
}
